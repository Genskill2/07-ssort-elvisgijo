void swap_max(int arr[],int l,int n) { 
         int max = arr[n];
         int t;
         for(int i=n;i<l;i++) { 
               if(arr[i]>max){
                      int t = arr[i];
                    }
                 }
            if(t>max){
                 int c = max;
                 max = t;
                 t = c;
               }
        }
